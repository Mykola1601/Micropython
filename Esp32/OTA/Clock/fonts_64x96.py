# === Параметри шрифта ===
FONT_WIDTH = 64
FONT_HEIGHT = 96

def draw_digit(epd, digit, x, y, color=0x00):
    """Відображає цифру на e-Paper дисплеї"""
    if digit not in digits_data:
        return
        
    bitmap = digits_data[digit]
    byte_width = FONT_WIDTH // 8
    
    # Малюємо піксель по пікселю
    for row in range(FONT_HEIGHT):
        for col_byte in range(byte_width):
            byte_index = row * byte_width + col_byte
            if byte_index < len(bitmap):
                byte_val = bitmap[byte_index]
                for bit in range(8):
                    if byte_val & (0x80 >> bit):
                        px_x = x + col_byte * 8 + bit
                        px_y = y + row
                        if 0 <= px_x < 296 and 0 <= px_y < 128:
                            epd.draw_point(px_x, px_y, color)



